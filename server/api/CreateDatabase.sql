/* ПОЛЬЗОВАТЕЛИ */
CREATE TABLE users(
  id BIGSERIAL PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(255) NOT NULL,
  password VARCHAR(500) NOT NULL,
  date_birthday DATE NOT NULL,
  sports_category  VARCHAR(255) NOT NULL,
  avatars VARCHAR(255)
);

/* ВИДЫ СПОРТА */
CREATE TABLE sports(
  id BIGSERIAL PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  trainer VARCHAR(300) NOT NULL
);

/* ПОЛЬЗОВАТЕЛИ И ВИДЫ СПОРТА */
CREATE TABLE users_sports(
  id BIGSERIAL PRIMARY KEY,
  user_id BIGSERIAL NOT NULL,
  sport_id BIGSERIAL NOT NULL,
  date_start DATE NOT NULL,
  date_end DATE,
  CONSTRAINT fk_user
    FOREIGN KEY(user_id)
	  REFERENCES users(id),
  CONSTRAINT fk_sport
    FOREIGN KEY(sport_id)
      REFERENCES sports(id)
);

/* СОРЕВНОВАНИЯ */
CREATE TABLE competitions(
  id BIGSERIAL PRIMARY KEY,
  sport_id BIGSERIAL NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(500),
  chief_judge VARCHAR(255) NOT NULL,
  date DATE NOT NULL,
  image VARCHAR(255),
  CONSTRAINT fk_sport
    FOREIGN KEY(sport_id)
      REFERENCES sports(id)
);

/* ПОЛЬЗОВАТЕЛИ И СОРЕВНОВАНИЯ */
CREATE TABLE users_competitions(
  id BIGSERIAL PRIMARY KEY,
  user_id BIGSERIAL NOT NULL,
  competition_id BIGSERIAL NOT NULL,
  start_group VARCHAR(255) NOT NULL,
  description VARCHAR(255) NOT NULL,
  results VARCHAR(255) NOT NULL,
  position INTEGER NOT NULL,
  CONSTRAINT fk_user
    FOREIGN KEY(user_id)
	  REFERENCES users(id),
  CONSTRAINT fk_competition
    FOREIGN KEY(competition_id)
      REFERENCES competitions(id)
);

